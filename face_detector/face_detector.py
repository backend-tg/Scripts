import cv2  # Импортируем библиотеку OpenCV для работы с изображениями и распознавания объектов

# Загружаем классификатор для обнаружения лиц, используя XML-файл с обученной моделью
face_cascade = cv2.CascadeClassifier("face_detector.xml")

# Загружаем изображение, на котором будем искать лица
img = cv2.imread("image.jpg")

# Обнаруживаем лица на изображении; параметры: масштабирование 1.1 и минимум соседей 4
faces = face_cascade.detectMultiScale(img, 1.1, 4)

# Перебираем координаты и размеры найденных лиц и рисуем прямоугольник вокруг каждого
for x, y, w, h in faces:
    cv2.rectangle(img, (x, y), (x + w, y + h), (255, 0, 0), 2)  # Прямоугольник синим цветом, толщина линии — 2 пикселя

# Сохраняем изображение с обнаруженными лицами в новый файл
cv2.imwrite("face_detected.png", img)

# Выводим сообщение об успешном сохранении изображения
print("Изображение успешно сохранено")
